// https://github.com/lmirosevic/GBDeviceInfo/blob/master/GBDeviceInfo/GBDeviceInfo_iOS.m
func GetIOSProductName(ProductType string) string {
	if a, ok := strings.CutPrefix(ProductType, "iPhone"); ok {
		if n, okk := iPhoneNames[a]; okk {
			return "iPhone " + n
		}
	}
	if a, ok := strings.CutPrefix(ProductType, "iPad"); ok {
		if n, okk := iPadNames[a]; okk {
			return "iPad " + n
		}
	}
	if a, ok := strings.CutPrefix(ProductType, "iPod"); ok {
		if n, okk := iPodNames[a]; okk {
			return "iPod " + n
		}
	}
	return ProductType
}

// ProductName示例：iPhone9,2, iPad7,5, iPod9,1
// ModelNumber示例：M或者N、F、P开头的5位字符串，N为官换机
func GetIOSURL(ProductName, ModelNumber string) string {
	if strings.HasPrefix(ProductName, "iPad") {
		return "https://raw.githubusercontent.com/orcastor/phone_images/master/ios/iPad.jpg"
	} else if strings.HasPrefix(ProductName, "iPod") {
		return "https://raw.githubusercontent.com/orcastor/phone_images/master/ios/iPod.jpg"
	}
	if len(ModelNumber) == 5 && ModelNumber[0] == 'N' {
		ModelNumber = "M" + ModelNumber[1:]
	}
	if color, ok := ModelNumber2Color[ModelNumber]; ok {
		return strings.ReplaceAll(fmt.Sprintf("https://raw.githubusercontent.com/orcastor/phone_images/master/ios/%s %s.jpg", ProductName, color), " ", "%20")
	}
	return GetIOSURL("iPhone 15 Pro Max", "MU783")
}

// 支持可变参数
// 0个参数返回默认值
// 1个参数传入product_name
// 2个参数传入ro.product.brand和ro.product.name
// 3个参数传入product_name和ro.product.brand和ro.product.name
func GetAndroidProductName(Names ...string) string {
	Product, Brand, Name := "", "", ""
	switch len(Names) {
	case 0:
		return ""
	case 1:
		Product = Names[0]
	case 2:
		Brand, Name = Names[0], Names[1]
	default:
		Product, Brand, Name = Names[0], Names[1], Names[2]
	}

	// 连接SQLite数据库
	db, err := sql.Open("sqlite3", androidDB)
	if err != nil {
		return fmt.Sprintf("%s %s", Brand, Name)
	}
	defer db.Close()

	// 查询数据
	var ProductName string
	if Product != Name {
		err = db.QueryRow("SELECT model FROM models WHERE model LIKE ? COLLATE NOCASE", fmt.Sprintf("%%%s%%%s%%", Product, Name)).Scan(&ProductName)
		if err == nil {
			return ProductName
		}
	}
	err = db.QueryRow("SELECT model FROM models WHERE model LIKE ? COLLATE NOCASE", fmt.Sprintf("%%%s%%%s%%", Brand, Name)).Scan(&ProductName)
	if err == nil {
		return ProductName
	}
	err = db.QueryRow("SELECT model FROM models WHERE model LIKE ? COLLATE NOCASE", fmt.Sprintf("%%%s%%", Product)).Scan(&ProductName)
	if err == nil {
		return ProductName
	}

	return "android"
}

